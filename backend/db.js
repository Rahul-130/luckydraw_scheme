const oracledb = require('oracledb');
const dotenv = require('dotenv');
dotenv.config();

oracledb.outFormat = oracledb.OUT_FORMAT_OBJECT;

let pool;
async function getPool() {
  if (pool) return pool;
  pool = await oracledb.createPool({
    user: process.env.ORACLE_USER,
    password: process.env.ORACLE_PASSWORD,
    connectString: `${process.env.ORACLE_HOST}:${process.env.ORACLE_PORT}/${process.env.ORACLE_SERVICE_NAME}`,
    poolMin: 1, poolMax: 4, poolIncrement: 1
  });
  return pool;
}
async function getConnection() {
  const p = await getPool();
  return p.getConnection();
}
async function initSchema() {
  const conn = await getConnection();
  try {
    await conn.execute(`BEGIN EXECUTE IMMEDIATE 'CREATE TABLE users (
      id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      name VARCHAR2(255) NOT NULL,
      phone VARCHAR2(20) NOT NULL,
      email VARCHAR2(255) UNIQUE NOT NULL,
      password_hash VARCHAR2(255) NOT NULL
    )'; EXCEPTION WHEN OTHERS THEN IF SQLCODE != -955 THEN RAISE; END IF; END;`);


    await conn.execute(`BEGIN EXECUTE IMMEDIATE 'CREATE TABLE books (
      id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      owner_id NUMBER NOT NULL,
      name VARCHAR2(255) NOT NULL,
      max_customers NUMBER NOT NULL,
      is_active NUMBER(1) DEFAULT 1 NOT NULL,
      start_month_iso VARCHAR2(7) NOT NULL,
      CONSTRAINT fk_books_owner FOREIGN KEY (owner_id) REFERENCES users(id)
    )'; EXCEPTION WHEN OTHERS THEN IF SQLCODE != -955 THEN RAISE; END IF; END;`);


    await conn.execute(`BEGIN EXECUTE IMMEDIATE 'CREATE TABLE customers (
      id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      book_id NUMBER NOT NULL,
      name VARCHAR2(255) NOT NULL,
      phone varchar2(255) NOT NULL,
      address VARCHAR2(255) NOT NULL,
      is_frozen NUMBER(1) DEFAULT 0 NOT NULL,
      created_at DATE DEFAULT SYSDATE NOT NULL,
      CONSTRAINT fk_customers_book FOREIGN KEY (book_id) REFERENCES books(id)
    )'; EXCEPTION WHEN OTHERS THEN IF SQLCODE != -955 THEN RAISE; END IF; END;`);


    await conn.execute(`BEGIN EXECUTE IMMEDIATE 'CREATE TABLE payments (
      id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      customer_id NUMBER NOT NULL,
      book_id NUMBER NOT NULL,
      month_iso VARCHAR2(7) NOT NULL,
      amount NUMBER NOT NULL,
      payment_date DATE DEFAULT SYSDATE NOT NULL,
      receipt_no VARCHAR2(255),
      is_luckydraw_winner NUMBER(1) DEFAULT 0 NOT NULL,
      CONSTRAINT fk_payments_book FOREIGN KEY (book_id) REFERENCES books(id),
      CONSTRAINT fk_payments_customer FOREIGN KEY (customer_id) REFERENCES customers(id),
      CONSTRAINT uq_payments_customer_book_month UNIQUE (customer_id, book_id, month_iso)
    )'; EXCEPTION WHEN OTHERS THEN IF SQLCODE != -955 THEN RAISE; END IF; END;`);


    await conn.execute(`BEGIN EXECUTE IMMEDIATE 'CREATE TABLE winner (
      id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      book_id NUMBER NOT NULL,
      book_name VARCHAR2(255),
      customer_id NUMBER NOT NULL,
      customer_name VARCHAR2(255),
      address VARCHAR2(255),
      phone VARCHAR2(50),
      is_manual NUMBER(1) DEFAULT 0 NOT NULL,
      win_date DATE DEFAULT SYSDATE NOT NULL,
      CONSTRAINT fk_winner_book FOREIGN KEY (book_id) REFERENCES books(id) ON DELETE CASCADE,
      CONSTRAINT fk_winner_customer FOREIGN KEY (customer_id) REFERENCES customers(id) ON DELETE CASCADE
    )'; EXCEPTION WHEN OTHERS THEN IF SQLCODE != -955 THEN RAISE; END IF; END;`);


    await conn.commit();
  } finally { await conn.close(); }
}
module.exports = { getConnection, initSchema, oracledb };